    public void getQuestion(){
        Next.setVisible(false);
        mark.setVisible(false);
        java.sql.Connection conn;
        try { 
            Class.forName("org.postgresql.Driver"); 
            String url = "jdbc:postgresql://localhost:5432/Peta_ITB"; 
            conn = DriverManager.getConnection(url, "postgres", "basdat");
            try (Statement s = conn.createStatement()) {
                ResultSet r = s.executeQuery("SELECT id, pertanyaan FROM question WHERE no_q = " + i);
                while(r.next()) {
                    id = r.getInt("id");
                    Soal.setText(r.getString("pertanyaan"));
                }
                r = s.executeQuery("SELECT nama FROM gedung WHERE id = " + id);
                while(r.next()) {
                    Answer = r.getString("nama");
                }
            }
            Soal.setVisible(true);
            conn.close(); 
            i++;
            if(i == 11){
                i = 1;
            }
        } 
        catch( ClassNotFoundException | SQLException e ) { 
        }
    }
